char	*ft_strcapitalize(char *str)
{
    int i;
    
    i = 0;
    
    if(str[i] >= 'a' || str[i] <= 'z')
    {
        str[i] -= 32;
        i++;
    }
    
    while(str[i])
    { 
        if(str[i] == ' ')
        {
            i++;
            if (str[i] >= 'a' && str[i] <= 'z')
            	str[i] -= 32;
        }
        else
        {
            if(str[i] >= 'A' && str[i] <= 'Z')
                str[i] += 32;  
        }

        i++;     
    }
    
    return (str);
}
