int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
    int i;
    
    i = 0;
    
    while (s1[i] && s2[i] && (i < n))
    {  
        if(s1[i] == s2[i])
            i++;
        else
            break;
    }
    
    if (i == n && i > 0)
        i--;
    
    if (s1[i] < s2[i])
        return (-1);
            
    if (s1[i] > s2[i])
        return (1);
    else       
        return (0);    
}
